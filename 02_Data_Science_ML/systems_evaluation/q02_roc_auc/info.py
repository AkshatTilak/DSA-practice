    # 1. Sort predictions and labels by probability in descending order
    data = sorted(zip(y_prob, y_true), key=lambda x: x[0], reverse=True)
    
    tp = 0  # True Positives count
    fp = 0  # False Positives count
    last_fp = 0
    last_tp = 0
    auc = 0.0
    
    # Count total positives and negatives
    total_pos = sum(y_true)
    total_neg = len(y_true) - total_pos
    
    if total_pos == 0 or total_neg == 0:
        return 0.0 # Handle edge cases with single-class data

    for i in range(len(data)):
        prob, label = data[i]
        
        if label == 1:
            tp += 1
        else:
            fp += 1
        
        # When we encounter a negative sample (or reach the end), 
